package com.java.mvc;

public class Student {

	private String rollno;
	private String name;
	
	public void setRollNo(String rollno)
	{
		this.rollno = rollno;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getRollNo()
	{
		return rollno;
	}
	
	public String getName()
	{
		return name;
	}
}
